# Create a function to generate frames
animate_plot <- function(data) {
  n <- length(data)
  for (i in 1:n) {
    par(bg = '#EEEEEE')
    plot(1:i, data[1:i], col.axis = "red", col = "red", lwd=10, pch= 62,
         type = "o-", ylim = c(0, 1), main = "Metric Example B", 
         xlab = "Frame Number", ylab = "Metric Output (Likelihood of Tackle)", 
         cex.lab=1.5, cex.axis=1.5, cex.main=1.5)
    Sys.sleep(0.1)  # Adjust the sleep time to control the animation speed
    ani.pause()
  }
}

